export interface Listing {
  /** Короткий уникальный slug, напр. 'kv-rokossovskogo' */
  id: string;
  /** Ключ категории (см. listingCategories) */
  category: string;
  /** Краткий заголовок объекта */
  title: string;
  /** Отформатированная цена, напр. '27 900 000 ₽' или '129 000 ₽ / мес' */
  price: string;
  /** Площадь, напр. '61 м²' */
  area: string;
  /** Полный адрес одной строкой (для отображения и для карты) */
  address: string;
  /** Метро/станция, если есть */
  metro?: string;
  /** 4–6 ключевых характеристик тегами (этаж, тип дома, ремонт и т.п.) */
  params: string[];
  /** Чистое описание, 2–4 предложения (деловой тон) */
  description: string;
}

