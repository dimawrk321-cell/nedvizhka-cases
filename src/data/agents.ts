/** Блок «Чем могу помочь»: вводная строка, пункты со списком, завершающая строка */
export interface HelpBlock {
  intro: string;
  items: string[];
  outro: string;
}

/** Блок «Банки-партнёры»: сплошной текст — вводная строка, список банков, завершающая строка */
export interface BanksBlock {
  intro: string;
  list: string[];
  outro: string;
}

/**
 * Личный телефон агента — переопределяет общий номер из site.ts В БЛОКЕ КОНТАКТОВ ПРОФИЛЯ.
 * Остальной сайт (шапка, футер, формы) продолжает брать номер из site.ts.
 */
export interface AgentPhone {
  /** Отображаемый номер, напр. «+7 (917) 398-24-03» */
  display: string;
  /** Ссылка tel:, напр. «tel:+79173982403» */
  href: string;
  /** Ссылка WhatsApp на тот же номер (в контактах профиля сейчас не показывается) */
  whatsappHref?: string;
}

/** Тексты формы заявки в профиле агента (переиспользует общий компонент LeadForm). */
export interface LeadFormCopy {
  /** Заголовок над формой */
  heading: string;
  /** Подзаголовок (женский/мужской род — под агента) */
  subheading: string;
  /** Текст кнопки отправки */
  submitText: string;
  /** Уникальный префикс id полей — не должен конфликтовать с другими формами сайта */
  idPrefix: string;
}

export interface Agent {
  /** Часть URL: /agents/<slug>/ */
  slug: string;
  name: string;
  /** Роль / зона ответственности */
  role: string;
  /** Специализация — теги */
  specialization: string[];
  /** Короткое описание для карточки (1–2 строки) */
  summary: string;
  /** Полное био — 3 абзаца */
  bio: string[];
  /** Регалии списком */
  credentials: string[];
  /** Блок «Чем могу помочь» — под био; null, если не задан */
  help: HelpBlock | null;
  /** Банки-партнёры — сплошным текстом (работа брокером); null, если не задан */
  banks: BanksBlock | null;
  /** Фото; null — плейсхолдер */
  photo: string | null;
  /** Личный телефон агента для блока контактов профиля; null — берётся общий из site.ts */
  phone: AgentPhone | null;
  /** Форма заявки в профиле (переиспользует LeadForm); null — формы нет */
  leadForm: LeadFormCopy | null;
  /** Услуги агента — карточки в профиле */
  services: Service[];
  /** Кейсы агента — карточки в профиле (сделки агента) */
  cases: CaseStudy[];
  /** Отзывы агента — лента в профиле */
  reviews: Review[];
  /** Сертификаты и дипломы — галерея с лайтбоксом внизу профиля */
  certificates: Certificate[];
}

